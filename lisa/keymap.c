void encoder_update_user(uint8_t index, bool clockwise) {
// left encoder
  if (index == 0) {
    switch(biton32(layer_state)) {

      case _NUMR0W:
        if (clockwise) {
          tap_code(KC_UP);
        } else {
          tap_code(KC_DOWN);
        } break;

      case _GAME2:
        if (clockwise) {
          tap_code(KC_UP);
        } else {
          tap_code(KC_DOWN);
        } break;

      default:
        if (clockwise) {
          tap_code(KC_WH_U);
        } else {
          tap_code(KC_WH_D);
        }
      }
    }

// right encoder
  else if (index == 1) {
    switch(biton32(layer_state)) {

      case _FNCTN: {
          if (clockwise) {
            // KC_VOLD up and down seems to be reversed for some reason
            tap_code(KC_VOLD);  
          } else {
            tap_code(KC_VOLU);
          } break;

      default:
          if (clockwise) {
            tap_code(KC_LEFT);
          } else {
            tap_code(KC_RGHT);
          }
        }
      }
    }
  }

